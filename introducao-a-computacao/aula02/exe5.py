#   Verificando ordenação

input1 = int(input("Digite o valor do primeiro inteiro: "))
input2 = int(input("Digite o valor do segundo inteiro: "))
input3 = int(input("Digite o valor do terceiro inteiro: "))

if input1 < input2 < input3:
    print("crescente")
else:
    print("não está em ordem crescente")

